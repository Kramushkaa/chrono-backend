export async function seedDatabase() {
  const client = await pool.connect();

  try {
    await client.query('BEGIN');
    await client.query(`DELETE FROM ${schema}.persons`);

    for (const person of sampleData) {
      // –í—Å—Ç–∞–≤–∫–∞ –õ–∏—á–Ω–æ—Å—Ç–∏ –±–µ–∑ legacy –ø–æ–ª–µ–π –¥–æ—Å—Ç–∏–∂–µ–Ω–∏–π
      await client.query(
        `INSERT INTO ${schema}.persons (id, name, birth_year, death_year, category, description, image_url)
         VALUES ($1, $2, $3, $4, $5, $6, $7)`,
        [
          person.id,
          person.name,
          person.birthYear,
          person.deathYear,
          person.category,
          person.description,
          person.imageUrl || null,
        ]
      );

      // –í—Å—Ç–∞–≤–∫–∞ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏–π –≤ –Ω–æ—Ä–º–∞–ª–∏–∑–æ–≤–∞–Ω–Ω—É—é —Ç–∞–±–ª–∏—Ü—É (—Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –µ—Å—Ç—å –≥–æ–¥)
      const years = [person.achievementYear1, person.achievementYear2, person.achievementYear3];
      for (let i = 0; i < Math.min(person.achievements.length, 3); i++) {
        const desc = person.achievements[i];
        const yr = years[i];
        if (typeof yr === 'number' && Number.isInteger(yr) && desc && desc.trim().length > 0) {
          await client.query(
            `INSERT INTO ${schema}.achievements (person_id, year, description, wikipedia_url, image_url)
             VALUES ($1, $2, $3, NULL, NULL)`,
            [person.id, yr, desc.trim()]
          );
        }
      }
    }

    await client.query('COMMIT');
    logger.info('Database seeded successfully', { action: 'seed_database' });
  } catch (error) {
    await client.query('ROLLBACK');
    const err = error instanceof Error ? error : new Error(String(error));
    logger.error('Error seeding database', { error: err, action: 'seed_database' });
    throw error;
  } finally {
    client.release();
  }
}

if (require.main === module) {
  seedDatabase().catch(err => {
    const e = err instanceof Error ? err : new Error(String(err));
    logger.error('Failed to seed database', { error: e, action: 'seed_database_main' });
    process.exit(1);
  });
}
